#!/usr/bin/env bash
# Bootstrap the Sonnet-via-LiteLLM eval stack.
#
# Idempotent — safe to re-run. Mirrors scripts/bootstrap-local-whatsapp.sh
# but targets /tmp/limbo-eval-sonnet and container names with -eval suffix.
#
# Required: ANTHROPIC_API_KEY in the environment. Fetch from 1Password:
#   ANTHROPIC_API_KEY=$(op item get zr5ul32zh2qcg7yqkddhxlhcgy --reveal --format json | \
#     jq -r '.fields[] | select(.id=="credential") | .value') \
#     bash evals/scripts/bootstrap-eval-sonnet.sh

set -euo pipefail

REPO_ROOT="$(cd "$(dirname "$0")/../.." && pwd)"
STATE_DIR="${STATE_DIR:-/tmp/limbo-eval-sonnet}"
CONFIG_DIR="$STATE_DIR/config"
COMPOSE_FILE="$REPO_ROOT/evals/docker-compose.eval-sonnet.yml"
SONNET_ENV_EXAMPLE="$REPO_ROOT/evals/config.eval.sonnet.env.example"

info() { printf '\033[0;36m[bootstrap-eval-sonnet]\033[0m %s\n' "$*"; }
fail() { printf '\033[0;31m[bootstrap-eval-sonnet]\033[0m %s\n' "$*" >&2; exit 1; }

command -v docker  >/dev/null || fail "docker CLI required"
command -v jq      >/dev/null || fail "jq required"
command -v openssl >/dev/null || fail "openssl required"
command -v curl    >/dev/null || fail "curl required"

: "${ANTHROPIC_API_KEY:?set ANTHROPIC_API_KEY (fetch from 1Password item limbo/anthropic-api-testing, UUID zr5ul32zh2qcg7yqkddhxlhcgy)}"

case "$ANTHROPIC_API_KEY" in
  sk-ant-api*-*) ;;
  sk-ant-oat-*) fail "ANTHROPIC_API_KEY is an OAuth token (sk-ant-oat-*); LiteLLM needs a real API key (sk-ant-api...)." ;;
  *) fail "ANTHROPIC_API_KEY doesn't look like an Anthropic API key (expected sk-ant-api<version>-...)." ;;
esac

mkdir -p "$STATE_DIR" "$CONFIG_DIR"

MASTER_KEY_FILE="$CONFIG_DIR/.litellm_master_key"
if [ ! -f "$MASTER_KEY_FILE" ]; then
  openssl rand -hex 32 > "$MASTER_KEY_FILE"; chmod 600 "$MASTER_KEY_FILE"
  info "generated LITELLM_MASTER_KEY"
fi
LITELLM_MASTER_KEY="$(tr -d '\n' < "$MASTER_KEY_FILE")"

GATEWAY_TOKEN_FILE="$CONFIG_DIR/.gateway_token"
if [ ! -f "$GATEWAY_TOKEN_FILE" ]; then
  openssl rand -hex 16 > "$GATEWAY_TOKEN_FILE"; chmod 600 "$GATEWAY_TOKEN_FILE"
  info "generated GATEWAY_TOKEN"
fi
GATEWAY_TOKEN="$(tr -d '\n' < "$GATEWAY_TOKEN_FILE")"

POSTGRES_PASSWORD_FILE="$CONFIG_DIR/.postgres_password"
if [ ! -f "$POSTGRES_PASSWORD_FILE" ]; then
  openssl rand -hex 24 > "$POSTGRES_PASSWORD_FILE"; chmod 600 "$POSTGRES_PASSWORD_FILE"
fi
POSTGRES_PASSWORD="$(tr -d '\n' < "$POSTGRES_PASSWORD_FILE")"

# Copy litellm config template verbatim.
cp "$REPO_ROOT/litellm-config.yaml.template" "$STATE_DIR/litellm-config.yaml"

cat > "$CONFIG_DIR/postgres.env" <<EOF
POSTGRES_USER=litellm
POSTGRES_PASSWORD=$POSTGRES_PASSWORD
POSTGRES_DB=litellm
EOF
chmod 600 "$CONFIG_DIR/postgres.env"

cat > "$CONFIG_DIR/litellm.env" <<EOF
ANTHROPIC_API_KEY=$ANTHROPIC_API_KEY
LITELLM_MASTER_KEY=$LITELLM_MASTER_KEY
DATABASE_URL=postgresql://litellm:$POSTGRES_PASSWORD@postgres-eval:5432/litellm
STORE_MODEL_IN_DB=True
EOF
chmod 600 "$CONFIG_DIR/litellm.env"

# Bring up postgres + litellm only; the limbo service is brought up by the
# run script after this bootstrap completes.
info "starting postgres-eval + litellm-eval..."
LIMBO_IMAGE="${LIMBO_IMAGE:-limbo:eval}" docker compose -f "$COMPOSE_FILE" up -d postgres-eval litellm-eval

info "waiting for litellm-eval to be healthy..."
deadline=$(( $(date +%s) + 180 ))
while :; do
  cid="$(docker compose -f "$COMPOSE_FILE" ps -q litellm-eval 2>/dev/null || true)"
  if [ -n "$cid" ]; then
    status="$(docker inspect -f '{{.State.Health.Status}}' "$cid" 2>/dev/null || echo 'starting')"
    [ "$status" = "healthy" ] && break
  fi
  if [ "$(date +%s)" -gt "$deadline" ]; then
    docker compose -f "$COMPOSE_FILE" logs litellm-eval | tail -50 >&2
    fail "litellm-eval did not become healthy"
  fi
  sleep 2
done
info "litellm-eval healthy"

VIRTUAL_KEY_FILE="$CONFIG_DIR/.litellm_virtual_key"
if [ ! -f "$VIRTUAL_KEY_FILE" ]; then
  info "minting virtual key via /key/generate..."
  resp="$(curl -fsS -X POST http://127.0.0.1:4100/key/generate \
    -H "Authorization: Bearer $LITELLM_MASTER_KEY" \
    -H 'Content-Type: application/json' \
    -d '{"models":["claude-sonnet-4-6"],"max_budget":50.0,"key_alias":"limbo-eval-sonnet"}')"
  virtual="$(echo "$resp" | jq -r '.key')"
  [ -n "$virtual" ] && [ "$virtual" != "null" ] || fail "/key/generate failed: $resp"
  printf '%s' "$virtual" > "$VIRTUAL_KEY_FILE"; chmod 600 "$VIRTUAL_KEY_FILE"
  info "virtual key saved (\$50 budget)"
fi
VIRTUAL_KEY="$(tr -d '\n' < "$VIRTUAL_KEY_FILE")"

# Materialize the limbo .env by concatenating the non-secret template with
# the generated secrets.
{
  cat "$SONNET_ENV_EXAMPLE"
  echo ""
  echo "# ── Generated by bootstrap-eval-sonnet.sh ──"
  echo "GATEWAY_TOKEN=$GATEWAY_TOKEN"
  echo "LLM_API_KEY=$VIRTUAL_KEY"
} > "$CONFIG_DIR/.env"
chmod 600 "$CONFIG_DIR/.env"

info "bootstrap complete."
info "state dir:  $STATE_DIR"
info "next:       LIMBO_IMAGE=limbo:eval docker compose -f $COMPOSE_FILE up -d --wait limbo-eval-sonnet"
